import Foundation

public class WCMathHelper {

 public init() {}

  public func add(a :Int, b: Int) -> Int {
     return a + b
  }

  func subtract(a :Int, b: Int) -> Int {
    return a - b
  }

  func multiply(a :Int, b: Int) -> Int {
    return a * b
  }
  
}
